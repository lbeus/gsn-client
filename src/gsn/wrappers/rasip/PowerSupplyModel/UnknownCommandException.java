/**
 * Thrown if an unknown command has been sent.
 */
public class UnknownCommandException extends SmartPowerException
{
	private static final long serialVersionUID = -6769672563604496619L;

	public UnknownCommandException()
	{
		super();
	}

	public UnknownCommandException(String message)
	{
		super(message);
	}
}
